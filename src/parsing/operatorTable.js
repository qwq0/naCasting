export const ot = new Map();
export const otFirst = new Map();
var operators = [
    ".", "[", "]",
    "(", ")",
    "!", "~", "-", "+", "++", "--",
    "*", "/", "%",
    "+", "-",
    "<<", ">>", ">>>",
    "<", "<=", ">", ">=",
    "==", "!=", "===", "!==",
    "&",
    "^",
    "|",
    "&&",
    "||",
    "?", ":",
    "=", "+=", "-=", "*=", "/=", "%=", "<<=", ">>=", ">>>=", "&=", "^=", "|="
];
operators.forEach(o =>
{
    ot.set(o, true);
    otFirst.set(o[0], true);
});